// difference between var, let and const.
// variable declaration ---var

//example---
var a;
console.log(a);  // let is a keyword to define a variable.
// you can re-declare and re-initialize to variable 'var'.
//re declaration of variable---var
var b;
console.log(b);

// variable assignment---let

//example
let c = 20;
console.log(c); // let is anotheer keyword to define a variable.
// we can re-assign some values.
// but we can't re declare the variable.

//re-assignment of variable---let
c = 30;
console.log(c);

//variable assignment--const

//example
const pi=3.14;// const is unique variable, it is used when we have to assign a uniuqe value like pi.
console.log(pi);
// we can't re-assign some values in const variable.
// but we can't re declare the variable.






