// Get the day of week based on the number using switch case.
let day=5;
switch(day){
        //for day 1
    case  (1):
        console.log("Sunday");
        break;
        //for day 2
    case  (2):
        console.log("Monday");
        break;
        //for day 3
    case  (3):
        console.log("Tuesday");
        break;
        //for day 4
    case  (4):
        console.log("Wednesday");
        break;
        //for day 5
    case  (5):
        console.log("Thursday");
        break;
        //for day 6
    case  (6):
        console.log("Friday");
        break;
        //for day 7
    case  (7):
        console.log("Saturday");
        break;
    default:
        //when day is not given
        console.log("Invalid day");   
        break;
}